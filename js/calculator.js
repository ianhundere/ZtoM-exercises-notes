

let first = parseInt(prompt("please enter a number: "));
let second = parseInt(prompt("please another a number: "));
operator = prompt("would you like to add, subtract, multiply or divide?");
if (operator === "add") {
    alert(`the sum of your numbers is ${(first + second)}`);
}
else if (operator === "subtract") {
    alert(`the sum of your numbers is ${(first - second)}`);
}
else if (operator === "multiply") {
    alert(`the sum of your numbers is ${(first - second)}`);
}
else if (operator === "divide") {
    alert(`the sum of your numbers is ${(first / second)}`);
}

